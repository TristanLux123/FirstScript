//Create V
var numOne = 1;
var numTwo = 1;
var numThree = 3;
var numFour = (numOne + numTwo + numThree);
//Addition
console.log("Addition");
console.log("--------");
console.log("Sum using variable: ", numFour);
console.log("Sum with no variables: ", 1 + 1 + 3);
console.log("\n");
//Subtraction
numFour = (numOne - numTwo - numThree);
console.log("Subtraction");
console.log("-----------");
console.log("Differnece using variable: ", numFour);
console.log("Difference with no variables: ", 1 - 1 - 3);
console.log("\n");
//Multiplication
numFour = (numOne * numTwo * numThree);
console.log("Multiplication");
console.log("--------------");
console.log("Product using variable: ", numFour);
console.log("Product with no variables: ", 1 * 1 * 3);
console.log("\n");
